       private void startCountingAnimation_customer() {
        int delay = 50; // Milliseconds between count updates
        timer = new Timer(delay, (ActionEvent e) -> {
            if (currentCount_customer < targetCount_customers) {
                currentCount_customer++;
                customer_label_tx.setText("Customers: " + currentCount_customer);
            } else {
                timer.stop();
            }
        });
        timer.start();
    }
       
       private void startCountingAnimation_loans() {
        int delay = 50; // Milliseconds between count updates
        timer = new Timer(delay, (ActionEvent e) -> {
            if (currentCount_loans < targetCount_Loans) {
                currentCount_loans++;
                loans_txt.setText("Loans: " + currentCount_loans);
            } else {
                timer.stop();
            }
        });
        timer.start();
    }
     

