     private void populateTable_loans() {
     D_LOANS d_loan = new D_LOANS ();
     DefaultTableModel model = (DefaultTableModel) data_table_loans.getModel();
     
     model.setRowCount(0);
     
      List <Model_Loan> loans = d_loan.display_loan();
      
      for (Model_Loan loan : loans){
          
          Object [] row = {
              
              loan.getLoan_id(),
              loan.getCustomer_name(),
              loan.getLoan(),
              loan.getInterest_rate(),
              loan.getPayback(),
              loan.getPayback_month(),
              loan.getDuration(),
              loan.getStatus()
              
          };
          
          model.addRow(row);
      }
}
     
     
